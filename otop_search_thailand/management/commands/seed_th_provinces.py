class Command(BaseCommand):
    help = "Seed 77 Thai provinces"

    def handle(self, *args, **kwargs):
        created = 0
        for name in TH_PROVINCES:
            _, was_created = Province.objects.get_or_create(name=name)
            if was_created:
                created += 1
        self.stdout.write(
            self.style.SUCCESS(
                f"Seeded provinces. New: {created}, Total: {Province.objects.count()}"
            )
        )
